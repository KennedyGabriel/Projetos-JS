const raio = 7.7;
const area = Math.PI * Math.pow(raio, 2); //PI é uma constante dentro de Math e 'pow' calcula a pontência

console.log(area.toFixed(2)); //.toFixed('número') serve para mostrar apenas a quantidade desejada de números após a vírgula
console.log(typeof Math); //Math é um objeto

